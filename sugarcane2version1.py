from scipy.stats import gamma as gamma_dist

def quantile_residuals(y_obs, mu, sigma):
    sh  = 1 / sigma**2;  sc = mu * sigma**2
    cdf = np.clip(gamma_dist.cdf(y_obs, a=sh, scale=sc), 1e-9, 1 - 1e-9)
    return norm.ppf(cdf)

qr = quantile_residuals(y, mu_hat, sigma_hat)

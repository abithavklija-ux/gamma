# Correctly combine gam_disp_terms by explicit addition
combined_gam_disp_terms = gam_disp_terms[0]
for term in gam_disp_terms[1:]:
    combined_gam_disp_terms += term
gam4d = GAM(combined_gam_disp_terms, distribution="gamma", link="log")
gam4d.gridsearch(X, pearson_residuals, progress=False)
sigma_hat = np.sqrt(np.abs(gam4d.predict(X))).clip(0.01, 5)

print(f"     ✓ M4 fitted | Mean model R\u00b2 = {gam4.statistics_['pseudo_r2']['explained_deviance']:.4f}")
print(f"     ✓ M4 dispersion model fitted | \u03c3 range = [{sigma_hat.min():.3f}, {sigma_hat.max():.3f}]")


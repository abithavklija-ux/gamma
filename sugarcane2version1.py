print(f"✓ Selected {len(cont_cols)} predictors:")
for i, col in enumerate(cont_cols, 1):
    print(f"  {i:2d}. {col}")

# Standardize features
scaler = StandardScaler()
X = scaler.fit_transform(df[cont_cols])

coverage = np.mean((y >= pi_lower) & (y <= pi_upper))
pi_width = pi_upper - pi_lower

# ── High/Low yield classification ────────────────────────────
threshold  = np.median(y)
y_class    = np.where(y >= threshold, "High_yield", "Low_yield")
pred_class = np.where(mu_m4 >= threshold, "High_yield", "Low_yield")
correct    = (y_class == pred_class)

# ── Risk quadrant assignment ──────────────────────────────────
mu_med  = np.median(mu_m4)
sig_med = np.median(sigma_hat)
def quadrant(mu, sig):
    if mu >= mu_med and sig >= sig_med: return "High_yield_High_risk"
    if mu <  mu_med and sig >= sig_med: return "Low_yield_High_risk"
    if mu >= mu_med and sig <  sig_med: return "High_yield_Low_risk"
    return "Low_yield_Low_risk"
risk_q = [quadrant(mu_m4[i], sigma_hat[i]) for i in range(n)]

# ── Assemble output dataset ───────────────────────────────────
out = df[["Plot_ID","Village","Season","Cultivar","Cycle","TSH_t_per_ha",
          "GNDVI","Total_stress_composite","Nutrient_balance_NB"]].copy()

out["mu_hat_M4"]          = np.round(mu_m4,    4)
out["sigma_hat_M4"]       = np.round(sigma_hat, 4)
out["PI_lower_95"]        = np.round(pi_lower,  4)
out["PI_upper_95"]        = np.round(pi_upper,  4)
out["PI_width"]           = np.round(pi_width,  4)
out["PI_coverage_flag"]   = np.where((y>=pi_lower)&(y<=pi_upper),"Inside_PI","Outside_PI")
out["Quantile_residual"]  = np.round(qr,        4)
out["Residual_flag"]      = np.where(np.abs(qr)<2,"Normal","Outlier")
out["Yield_class_obs"]    = y_class
out["Yield_class_pred"]   = pred_class
out["Classification_correct"] = correct.astype(str)
out["Risk_quadrant"]      = risk_q
out["mu_hat_M1"]          = np.round(mu_m1, 4)
out["mu_hat_M2"]          = np.round(mu_m2, 4)
out["mu_hat_M3"]          = np.round(mu_m3, 4)

out.to_csv("/tmp/output_dataset.csv", index=False)
print(f"Output dataset: {len(out)} rows × {len(out.columns)} cols")

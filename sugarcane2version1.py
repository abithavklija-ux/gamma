# ─────────────────────────────────────────────────────────────
# 3. FEATURE ENGINEERING
# ─────────────────────────────────────────────────────────────

# GNDVI from Landsat bands
df["GNDVI"] = (df["band_NIR"] - df["band_Green"]) / (
               df["band_NIR"] + df["band_Green"] + 1e-9)

# Stress indices (all 0–1 bounded)
df["drought_index"]    = np.where(df["precip_mm"] < 600,
                           (600 - df["precip_mm"]) / 600, 0)
df["excess_rain_index"]= np.where(df["precip_mm"] > 1500,
                           (df["precip_mm"] - 1500) / 1500, 0)
df["heat_stress_index"]= np.where(df["tmax_c"] > 35,
                           (df["tmax_c"] - 35) / 10, 0).clip(0, 1)
df["soil_degradation_index"] = (df["bulk_density"] - 1.0) / 0.8
df["pest_pressure_index"]    = df["borer_damage_pct"] / 40
df["disease_pressure_index"] = df["disease_incidence_pct"] / 30

# Combined stress score
df["total_stress"] = (
    df["drought_index"] + df["excess_rain_index"] +
    df["heat_stress_index"] + df["soil_degradation_index"] +
    df["pest_pressure_index"] + df["disease_pressure_index"]
) / 6

# Nutrient mediator
df["nutrient_balance"] = (df["N_content"] / 4 +
                          df["P_content"] / 0.6 +
                          df["K_content"] / 3.5) / 3

# Simulate TSH: gamma with mean and dispersion driven by covariates
mu_true = np.exp(
    1.5
    + 0.8  * df["GNDVI"]
    + 0.4  * (df["precip_mm"] - 900) / 500
    - 1.2  * df["total_stress"]
    + 0.5  * df["nutrient_balance"]
    + np.where(df["cycle"] == "plant", 0.2, 0)
    + np.where(df["location"] == "Maharashtra", 0.15, 0)
)
sigma_true = np.exp(
    -0.5
    + 0.6 * df["total_stress"]
    - 0.4 * df["GNDVI"]
    + 0.3 * (1 - df["nutrient_balance"])
)
# Sample TSH from Gamma(mu, sigma)
shape_true = 1 / sigma_true**2
scale_true = mu_true * sigma_true**2
df["TSH"] = gamma_dist.rvs(a=shape_true, scale=scale_true)


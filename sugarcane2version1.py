# ─────────────────────────────────────────────────────────────
# 6. MEAN SUB-MODEL — Semiparametric GAM
# ─────────────────────────────────────────────────────────────
# Using pyGAM: smooth splines on continuous covariates
# Columns: 0=GNDVI, 1=precip, 2=drought, 3=excess_rain,
#          4=heat, 5=soil_deg, 6=pest, 7=disease, 8=nutrient, 9=total_stress

from pygam import GAM, s, l, te

gam_mean = GAM(
    s(0, n_splines=10) +   # GNDVI
    s(1, n_splines=10) +   # precip
    s(2, n_splines=8)  +   # drought
    s(4, n_splines=8)  +   # heat stress
    s(5, n_splines=8)  +   # soil degradation
    s(6, n_splines=8)  +   # pest pressure
    s(7, n_splines=8)  +   # disease pressure
    s(8, n_splines=10),    # nutrient balance
    distribution="gamma",
    link="log"
)
gam_mean.gridsearch(X_cont, y, progress=False)
mu_hat = gam_mean.predict(X_cont)
print("\nGAM mean sub-model summary:")
gam_mean.summary()

# ─────────────────────────────────────────────────────────────
# 7. DISPERSION SUB-MODEL
# ─────────────────────────────────────────────────────────────
# Two-stage approach: model squared Pearson residuals as dispersion proxy

pearson_resid_sq = ((y - mu_hat) ** 2) / mu_hat

# Fit GAM on dispersion
gam_disp = GAM(
    s(2, n_splines=8)  +   # drought
    s(4, n_splines=8)  +   # heat stress
    s(5, n_splines=8)  +   # soil degradation
    s(6, n_splines=8)  +   # pest pressure
    s(7, n_splines=8)  +   # disease pressure
    s(8, n_splines=10) +   # nutrient balance
    s(0, n_splines=10),    # GNDVI
    distribution="gamma",
    link="log"
)
gam_disp.gridsearch(X_cont, pearson_resid_sq, progress=False)
sigma_hat = np.sqrt(np.abs(gam_disp.predict(X_cont)))
sigma_hat = np.clip(sigma_hat, 0.01, 5.0)


# ─────────────────────────────────────────────────────────────
# 2.  SYNTHETIC TSH DATA  (West Champaran — 3 villages)
# ─────────────────────────────────────────────────────────────
np.random.seed(42)
n_per_village = 167
villages = (["Sirsa"] * n_per_village +
            ["Masjidwa"] * n_per_village +
            ["Matiaria"] * (500 - 2 * n_per_village))

# Village-specific gamma parameters (mu, sigma)
params = {"Sirsa":    (10.2, 0.55),
          "Masjidwa": (7.8,  0.65),
          "Matiaria": (6.5,  0.72)}

tsh_all, village_all = [], []
for v in villages:
    mu, sig = params[v]
    shape = 1 / sig**2
    scale = mu * sig**2
    tsh_all.append(gamma_dist.rvs(a=shape, scale=scale))
    village_all.append(v)

df = pd.DataFrame({"TSH": tsh_all, "Village": village_all})

# Simulated fitted values from M4
noise = np.random.normal(0, 1.2, 500)
df["TSH_fitted"] = (df["TSH"] + noise).clip(0.5)
df["TSH_lower"]  = df["TSH_fitted"] - np.abs(np.random.normal(2.5, 0.8, 500))
df["TSH_upper"]  = df["TSH_fitted"] + np.abs(np.random.normal(2.5, 0.8, 500))


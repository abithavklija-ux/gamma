if len(disp_indices) > 0:
    gam_disp_terms = sum([s(i) for i in disp_indices])
    gam4d = GAM(gam_disp_terms, distribution="gamma", link="log")
    gam4d.gridsearch(X, pearson_residuals, progress=False)
    sigma_hat = np.sqrt(np.abs(gam4d.predict(X))).clip(0.01, 5)
else:
    # Fallback if no stress indices available
    sigma_hat = np.ones(len(y))

print(f"      ✓ M4 fitted | Mean model R² = {gam4.statistics_['pseudo_r2']['explained_deviance']:.4f}")
print(f"      ✓ M4 dispersion model fitted | σ range = [{sigma_hat.min():.3f}, {sigma_hat.max():.3f}]")


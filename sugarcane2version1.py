    mae = mean_absolute_error(y, mu_m)
    r2 = r2_score(y, mu_m)
    mape = np.mean(np.abs((y - mu_m) / y)) * 100

    rmse_list.append(rmse)
    mae_list.append(mae)
    r2_list.append(r2)
    mape_list.append(mape)

    print(f"\n{label}: {model_labels[i].replace(chr(10), ' ')}")
    print(f"  RMSE:  {rmse:.4f} t/ha")
    print(f"  MAE:   {mae:.4f} t/ha")
    print(f"  R²:    {r2:.4f}")
    print(f"  MAPE:  {mape:.2f}%")


print(f"  Response mean: {y.mean():.2f} ± {y.std():.2f} t/ha")

# Add constant for GLM
Xc = sm.add_constant(X)

# ============================================================
# 4. FIT ALL FOUR MODELS
# ============================================================

print("\n" + "=" * 80)
print(" FITTING MODELS")
print("=" * 80)

# MODEL 1: Normal Linear Model on log(TSH)
print("\n[1/4] Fitting M1: Normal Linear Model (log-transformed)...")
m1 = sm.OLS(np.log(y), Xc).fit()
mu_m1 = np.exp(m1.fittedvalues)
print(f"     ✓ M1 fitted | R² = {m1.rsquared:.4f}")

# MODEL 2: Parametric Gamma GLM (constant dispersion)
print("\n[2/4] Fitting M2: Gamma GLM (log link)...")
m2 = sm.GLM(y, Xc, family=Gamma(link=LogLink())).fit()
mu_m2 = m2.fittedvalues
print(f"     ✓ M2 fitted | Deviance = {m2.deviance:.2f}")

# MODEL 3: Semiparametric GAM (mean only)
print("\n[3/4] Fitting M3: Semiparametric GAM (mean only)...")
# Create GAM with smooth terms for each predictor
gam_terms = [s(i) for i in range(len(cont_cols))]
gam3 = GAM(sum(gam_terms), distribution="gamma", link="log")
gam3.gridsearch(X, y, progress=False)
mu_m3 = gam3.predict(X)
print(f"     ✓ M3 fitted | Pseudo R² = {gam3.statistics_['pseudo_r2']['explained_deviance']:.4f}")

# MODEL 4: Proposed Heteroscedastic GA (mean + dispersion)
print("\n[4/4] Fitting M4: Proposed Heteroscedastic GA...")

# Step 1: Fit mean model
gam4 = GAM(sum(gam_terms), distribution="gamma", link="log")
gam4.gridsearch(X, y, progress=False)
mu_m4 = gam4.predict(X)

# Step 2: Fit dispersion model
pearson_residuals = ((y - mu_m4)**2) / (mu_m4 + 1e-9)
# Select important features for dispersion (stress indices)
disp_indices = [cont_cols.index(col) for col in
                ["Heat_stress_index_I_heat", "Soil_degradation_I_soil",
                 "Pest_pressure_I_pest", "Disease_pressure_I_disease",
                 "Total_stress_composite", "Drought_index_I_drought", "GNDVI"]
                if col in cont_cols]

gam_disp_terms = [s(i) for i in disp_indices]
gam4d = GAM(sum(gam_disp_terms), distribution="gamma", link="log")
gam4d.gridsearch(X, pearson_residuals, progress=False)
sigma_hat = np.sqrt(np.abs(gam4d.predict(X))).clip(0.01, 5)

print(f"     ✓ M4 fitted | Mean model R² = {gam4.statistics_['pseudo_r2']['explained_deviance']:.4f}")

# ─────────────────────────────────────────────────────────────
# 8. QUANTILE RESIDUALS
# ─────────────────────────────────────────────────────────────

def quantile_residuals_gamma(y_obs, mu, sigma):
    """
    Compute randomised quantile residuals for gamma distribution.
    These should be ~ N(0,1) if the model is correct.
    """
    shape = 1.0 / (sigma ** 2)
    scale = mu * (sigma ** 2)
    cdf_vals = gamma_dist.cdf(y_obs, a=shape, scale=scale)
    cdf_vals = np.clip(cdf_vals, 1e-9, 1 - 1e-9)
    return norm.ppf(cdf_vals)

qr = quantile_residuals_gamma(y, mu_hat, sigma_hat)
print("\nQuantile residuals — mean:", round(qr.mean(), 4),
      " std:", round(qr.std(), 4))


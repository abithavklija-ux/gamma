print(f"Shapiro-Wilk test: W={sw_stat:.4f}, p={sw_p:.4f}",
      "  [good if p > 0.05]")

# ─────────────────────────────────────────────────────────────
# 9. PREDICTION INTERVALS
# ─────────────────────────────────────────────────────────────

def gamma_prediction_interval(mu, sigma, alpha=0.05):
    shape = 1.0 / sigma**2
    scale = mu * sigma**2
    lower = gamma_dist.ppf(alpha / 2,     a=shape, scale=scale)
    upper = gamma_dist.ppf(1 - alpha / 2, a=shape, scale=scale)
    return lower, upper

lower_pi, upper_pi = gamma_prediction_interval(mu_hat, sigma_hat)
coverage = np.mean((y >= lower_pi) & (y <= upper_pi))
print(f"\n95% PI empirical coverage: {coverage:.3f}  [target \u2248 0.95]")


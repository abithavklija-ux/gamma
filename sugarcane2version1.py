# 10. MODEL COMPARISON METRICS
# ─────────────────────────────────────────────────────────────

def gamma_log_likelihood(y_obs, mu, sigma):
    shape = 1.0 / sigma**2
    scale = mu * sigma**2
    return np.sum(gamma_dist.logpdf(y_obs, a=shape, scale=scale))

def aic(loglik, k):
    return -2 * loglik + 2 * k

def bic(loglik, k, n):
    return -2 * loglik + k * np.log(n)

# Proposed model
ll_prop = gamma_log_likelihood(y, mu_hat, sigma_hat)

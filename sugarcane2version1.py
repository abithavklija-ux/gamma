# ─────────────────────────────────────────────────────────────
# STEP 6 — MODEL COMPARISON METRICS TABLE
# ─────────────────────────────────────────────────────────────
def gamma_loglik(y_obs, mu, sigma):
    sh = 1 / sigma**2;  sc = mu * sigma**2
    return np.sum(gamma_dist.logpdf(y_obs, a=sh, scale=sc))

def aic_val(ll, k): return -2 * ll + 2 * k
def bic_val(ll, k, n): return -2 * ll + k * np.log(n)

